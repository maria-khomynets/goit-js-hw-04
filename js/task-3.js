function changeEven(numbers, value) {
  for (let i = 0; i < numbers.length; i += 1) {
    if (numbers[i] % 2 === 0) {
      numbers[i] = numbers[i] + value;
    }
  }
}
changeEven([17, 24, 68, 31, 42], 100);

const planets = ['Earth', 'Mars', 'Venus', 'Jupiter'];

